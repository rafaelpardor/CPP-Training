#include <iostream>

int main()
{
    int numb1 = 27;
    int numb2 = 1;
    int numb3 = 6;
    int numb4 = 12;

    std::cout << "Suma: " << numb1 + numb2 << std::endl;
    std::cout << "Resta: " << numb4 - numb3 << std::endl;
    std::cout << "Resta: " << numb3 - numb4 << std::endl;
    std::cout << "Mult: " << numb3 * numb4 << std::endl;
    std::cout << "Div: " << numb4 / numb3 << std::endl;
    std::cout << "Modulo: " << numb3 % 2 << std::endl;

    return 0;
}